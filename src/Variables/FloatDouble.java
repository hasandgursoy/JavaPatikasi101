package Variables;

public class FloatDouble {

    public static void main(String[] args) {

        float vFloat = 3.14f; // 32 byte
        double vDouble = 3.14; // 64 byte

        // Double hassas matematik işlemleri için birebir çok güçlü bir yapıdır.

        System.out.println(vFloat);
        System.out.println(vDouble);



    }

}
